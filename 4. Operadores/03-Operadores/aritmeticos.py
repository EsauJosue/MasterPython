# Operadores aritmeticos

num1 = input("Ingrese el primer valor: ")
num2 = input("Ingrese el segundo valor: ") #Operador de asignaci√≥n 

resta = int(num1) - int(num2)
suma = int(num1) + int(num2)
multiplicacion = int(num1) * int(num2)
division = int(num1) / int(num2)
#El resto es lo que sobra de una division
resto = int(num1) % int(num2)



print("El resultado de la resta es la siguiente: "+ str(resta))
print("El resultado de la suma es la siguiente: "+ str(suma))
print("El resultado de la multiplicacion es la siguiente: "+ str(multiplicacion))
print("El resultado de la division es la siguiente: "+ str(division))
print("El resultado de lo que sobra es la siguiente: "+ str(resto))


